export type TicketStatus = 'queued' | 'diagnosing' | 'waiting-for-parts' | 'repairing' | 'pickup' | 'completed';
export type DeviceType = 'phone' | 'laptop' | 'tablet' | 'desktop' | 'other' | 'tv' | 'speaker';

export interface NavLinkType {
    name: TicketStatus;
    href: string;
    icon: React.ElementType;
    count: number;
}

export interface TicketCardType {
    id: string; // ticket_id
    ticket_number: string;
    customer_name: string;
    device_type: DeviceType;
    device_brand: string;
    device_model: string;
    description: string;
    etr: Date | null;
    status: TicketStatus;
    total_cost: number;
    paid: boolean;
}

export interface TicketType {
    id: string;
    customer_id: string; // link to customer's id
    device_type: DeviceType;
    device_brand: string;
    device_model: string;
    description: string;
    etr: Date | null;
    photo: string | null;
    timeline: {
        created_at: Date;
        updated_at: Date;
    };
    payment: {
        repair_cost: number;
        parts_cost: number;
        tax: number;
        total: number;
        paid: boolean;
    };
    status: TicketStatus;
    ticket_number: string; // this is generated by the system. It is used to identify device repair tickets.
}
